function paraFilter($para) {
    $para_filter = array();
    foreach ($para as $key=>$val) {
        if($key == "sign" || $key == "sign_type" || $val == "")continue;
        else $para_filter[$key] = $para[$key];
    }
    return $para_filter;
}
/**
 * 对数组排序
 * @param $para 排序前的数组
 * return 排序后的数组
 */
function argSort($para) {
    ksort($para);
    reset($para);
    return $para;
}
