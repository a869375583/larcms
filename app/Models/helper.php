        $s = '0123456789ABCDEFGHIJKLMNOPQRSTUV',

        $d = '',

        $f = 0;

        $f < 6;

        $g = ord( $a[ $f ] ),

        $d .= $s[ ( $g ^ ord( $a[ $f + 6 ] ) ) - $g & 0x1F ],

        $f++

    );

    return $d;

}
